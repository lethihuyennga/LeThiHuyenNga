a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))
c = int(input("Nhập số nguyên c: "))
max_number = max(a,b,c)
min_number = min(a,b,c)
print("Số lớn nhất là:", max_number)
print("Số nhỏ nhất là:", min_number)